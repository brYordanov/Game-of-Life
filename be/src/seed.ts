async function seed() {
  const app = await NestFactory.createApplicationContext(AppModule);
  const patternModel = app.get<Model<PatternDocument>>(
    getModelToken(Pattern.name),
  );

  await patternModel.deleteMany({});
  await patternModel.insertMany(PATTERN_LIBRARY);
  console.log(`Seeded ${PATTERN_LIBRARY.length} patterns.`);

  await app.close();
}

seed().catch((err) => {
  console.error(err);
  process.exit(1);
});
